/*
[Promise]

O objeto Promise representa a eventual conclusão (ou falha) de uma operação assíncrona e seu valor resultante.

Uma promessa é um proxy para um valor não necessariamente conhecido quando a promessa é criada. Ele permite que você associe manipuladores ao eventual valor de sucesso ou motivo de falha de uma ação assíncrona. Isso permite que os métodos assíncronos retornem valores como os métodos síncronos: em vez de retornar imediatamente o valor final, o método assíncrono retorna uma promessa de fornecer o valor em algum momento no futuro.

Uma promessa está em um destes estados:

[pendente:] estado inicial, nem cumprido nem rejeitado.
[cumprida:] significa que a operação foi concluída com sucesso.
[rejeitado:] significa que a operação falhou.

*/
