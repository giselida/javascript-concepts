/*
[Logical OR] (||)

O operador lógico OR (||) (disjunção lógica) para um conjunto de operandos é verdadeiro se e somente se um ou mais de seus operandos for verdadeiro. Normalmente é usado com valores booleanos (lógicos). Quando for, ele retorna um valor booleano. No entanto, o || na verdade, retorna o valor de um dos operandos especificados; portanto, se esse operador for usado com valores não booleanos, ele retornará um valor não booleano.
*/

const a = 3;
const b = -2;

console.log(a > 0 || b > 0); // true
