/*
[Logical AND] (&&)

O operador lógico AND (&&) (conjunção lógica) para um conjunto de operandos booleanos será verdadeiro se e somente se todos os operandos forem verdadeiros. Caso contrário, será falso.

De forma mais geral, o operador retorna o valor do primeiro operando falso encontrado ao avaliar da esquerda para a direita, ou o valor do último operando se todos forem verdadeiros.

*/

const a = 3;
const b = -2;

console.log(a > 0 && b > 0); //false
